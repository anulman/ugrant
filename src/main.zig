fn mergeServiceDefinition(base: ServiceDefinition, overlay: ServiceDefinition) ServiceDefinition {
    var out = base;
    if (overlay.provider != null) out.provider = overlay.provider;
    if (overlay.auth_url != null) out.auth_url = overlay.auth_url;
    if (overlay.token_url != null) out.token_url = overlay.token_url;
    if (overlay.scope != null) out.scope = overlay.scope;
    if (overlay.redirect_uri != null) out.redirect_uri = overlay.redirect_uri;
    if (overlay.env_kind != null) out.env_kind = overlay.env_kind;
    if (overlay.base_url != null) out.base_url = overlay.base_url;
    if (overlay.model != null) out.model = overlay.model;
    if (overlay.audience != null) out.audience = overlay.audience;
    if (overlay.issuer != null) out.issuer = overlay.issuer;
    if (overlay.discovery_url != null) out.discovery_url = overlay.discovery_url;
    return out;
}

fn resolveServicePreset(name: []const u8) !ServiceDefinition {
    for (service_presets) |preset| {
        if (std.ascii.eqlIgnoreCase(name, preset.key)) {
            return .{
                .provider = preset.provider,
                .auth_url = preset.auth_url,
                .token_url = preset.token_url,
                .scope = preset.scope,
                .redirect_uri = preset.redirect_uri,
                .env_kind = preset.env_kind,
                .base_url = preset.base_url,
                .model = preset.model,
                .audience = preset.audience,
            };
        }
        for (preset.aliases) |alias| {
            if (std.ascii.eqlIgnoreCase(name, alias)) {
                return .{
                    .provider = preset.provider,
                    .auth_url = preset.auth_url,
                    .token_url = preset.token_url,
                    .scope = preset.scope,
                    .redirect_uri = preset.redirect_uri,
                    .env_kind = preset.env_kind,
                    .base_url = preset.base_url,
                    .model = preset.model,
                    .audience = preset.audience,
                };
            }
        }
    }
    return error.UnknownServicePreset;
}

fn buildDiscoveryUrl(allocator: std.mem.Allocator, issuer_or_url: []const u8) ![]const u8 {
    const trimmed = std.mem.trim(u8, issuer_or_url, " \r\n\t/");
    if (std.mem.endsWith(u8, trimmed, ".well-known/openid-configuration")) {
        return allocator.dupe(u8, trimmed);
    }
    return std.fmt.allocPrint(allocator, "{s}/.well-known/openid-configuration", .{trimmed});
}

fn inferProviderFromUrl(url: []const u8) []const u8 {
    if (std.mem.indexOf(u8, url, "google")) |_| return "google";
    if (std.mem.indexOf(u8, url, "openai")) |_| return "openai";
    return "oidc";
}

fn inferEnvKind(provider: []const u8, issuer: ?[]const u8) []const u8 {
    if (std.mem.eql(u8, provider, "openai")) return "openai";
    if (issuer) |iss| {
        if (std.mem.indexOf(u8, iss, "openai")) |_| return "openai";
    }
    return "generic";
}

fn parseDiscoveredService(allocator: std.mem.Allocator, discovery_url: []const u8, document: []const u8) !ServiceDefinition {
    const parsed = try std.json.parseFromSlice(std.json.Value, allocator, document, .{});
    defer parsed.deinit();
    const obj = parsed.value.object;
    const issuer = if (obj.get("issuer")) |v| try allocator.dupe(u8, v.string) else null;
    const auth_url = if (obj.get("authorization_endpoint")) |v| try allocator.dupe(u8, v.string) else null;
    const token_url = if (obj.get("token_endpoint")) |v| try allocator.dupe(u8, v.string) else null;
    if (auth_url == null or token_url == null) return error.InvalidDiscoveryDocument;

    const provider = try allocator.dupe(u8, inferProviderFromUrl(issuer orelse discovery_url));
    const env_kind = try allocator.dupe(u8, inferEnvKind(provider, issuer));
    const base_url = if (std.mem.eql(u8, env_kind, "openai"))
        try allocator.dupe(u8, "https://api.openai.com/v1")
    else
        null;
    const scope = if (std.mem.eql(u8, provider, "openai"))
        try allocator.dupe(u8, "openid profile email offline_access")
    else
        null;

    return .{
        .provider = provider,
        .auth_url = auth_url,
        .token_url = token_url,
        .scope = scope,
        .redirect_uri = default_redirect_uri,
        .env_kind = env_kind,
        .base_url = base_url,
        .issuer = issuer,
        .discovery_url = try allocator.dupe(u8, discovery_url),
    };
}

fn discoverService(allocator: std.mem.Allocator, issuer_or_url: []const u8) !ServiceDefinition {
    const discovery_url = try buildDiscoveryUrl(allocator, issuer_or_url);
    const script =
        "import sys, urllib.request\n" ++
        "with urllib.request.urlopen(sys.argv[1]) as resp:\n" ++
        "    sys.stdout.buffer.write(resp.read())\n";
    const result = try std.process.Child.run(.{
        .allocator = allocator,
        .argv = &.{ "python3", "-c", script, discovery_url },
        .max_output_bytes = 256 * 1024,
    });
    defer allocator.free(result.stdout);
    defer allocator.free(result.stderr);
    switch (result.term) {
        .Exited => |code_int| if (code_int != 0) return error.DiscoveryFetchFailed,
        else => return error.DiscoveryFetchFailed,
    }
    return try parseDiscoveredService(allocator, discovery_url, result.stdout);
}

pub fn main() !void {
    var gpa = std.heap.GeneralPurposeAllocator(.{}){};
    defer _ = gpa.deinit();
    const allocator = gpa.allocator();

    const args = try std.process.argsAlloc(allocator);
    defer std.process.argsFree(allocator, args);

    var out_buf: [32768]u8 = undefined;
    var err_buf: [16384]u8 = undefined;
    var out = std.fs.File.stdout().writer(&out_buf);
    var err = std.fs.File.stderr().writer(&err_buf);

    if (args.len <= 1) {
        try out.interface.writeAll(usage_text);
        try out.interface.flush();
        return;
    }

    const cmd = args[1];
    if (std.mem.eql(u8, cmd, "--help") or std.mem.eql(u8, cmd, "-h") or std.mem.eql(u8, cmd, "help")) {
        try out.interface.writeAll(usage_text);
        try out.interface.flush();
        return;
    }
    if (std.mem.eql(u8, cmd, "version") or std.mem.eql(u8, cmd, "--version")) {
        try out.interface.print("ugrant {s}\n", .{version});
        try out.interface.flush();
        return;
    }

    if (std.mem.eql(u8, cmd, "init")) {
        try cmdInit(allocator, args[2..], &out.interface, &err.interface);
    } else if (std.mem.eql(u8, cmd, "profile")) {
        try cmdProfile(allocator, args[2..], &out.interface, &err.interface);
    } else if (std.mem.eql(u8, cmd, "login")) {
        try cmdLogin(allocator, args[2..], &out.interface, &err.interface);
    } else if (std.mem.eql(u8, cmd, "env")) {
        try cmdEnv(allocator, args[2..], &out.interface, &err.interface);
    } else if (std.mem.eql(u8, cmd, "exec")) {
        try cmdExec(allocator, args[2..], &out.interface, &err.interface);
    } else if (std.mem.eql(u8, cmd, "revoke")) {
        try cmdRevoke(allocator, args[2..], &out.interface, &err.interface);
    } else if (std.mem.eql(u8, cmd, "rekey")) {
        try cmdRekey(allocator, args[2..], &out.interface, &err.interface);
    } else if (std.mem.eql(u8, cmd, "status")) {
        try cmdStatus(allocator, args[2..], &out.interface, &err.interface);
    } else if (std.mem.eql(u8, cmd, "doctor")) {
        try cmdDoctor(allocator, args[2..], &out.interface, &err.interface);
    } else {
        try err.interface.print("ugrant: command '{s}' is not implemented yet.\n", .{cmd});
        try err.interface.flush();
        std.process.exit(2);
    }

    try out.interface.flush();
    try err.interface.flush();
}

fn cmdInit(allocator: std.mem.Allocator, args: []const []const u8, out: *std.Io.Writer, err: *std.Io.Writer) !void {
    var requested_backend: ?[]const u8 = null;
    var allow_insecure = false;
    var i: usize = 0;
    while (i < args.len) : (i += 1) {
        const arg = args[i];
        if (std.mem.eql(u8, arg, "--allow-insecure-keyfile") or std.mem.eql(u8, arg, "--insecure-keyfile")) allow_insecure = true else if (std.mem.eql(u8, arg, "--backend")) {
            i += 1;
            if (i >= args.len) {
                try err.writeAll("usage: ugrant init [--backend <name>] [--allow-insecure-keyfile]\n");
                std.process.exit(2);
            }
            requested_backend = args[i];
        } else if (std.mem.eql(u8, arg, "--help") or std.mem.eql(u8, arg, "-h")) {
            try out.writeAll("usage: ugrant init [--backend <name>] [--allow-insecure-keyfile]\n");
            return;
        } else {
            try err.writeAll("usage: ugrant init [--backend <name>] [--allow-insecure-keyfile]\n");
            std.process.exit(2);
        }
    }

    const paths = try resolvePaths(allocator);
    defer paths.deinit(allocator);

    try ensureParentDirs(paths);
    try ensureConfig(paths.config_path);

    const wrapped = try initOrLoadKeys(allocator, paths.keys_path, requested_backend, allow_insecure);
    defer freeWrappedDekRecord(allocator, wrapped);

    const dek = try unwrapDek(allocator, wrapped);
    defer allocator.free(dek);

    const db = try openDb(paths.db_path);
    defer _ = c.sqlite3_close(db);
    try ensureSchema(db);

    try out.print("initialized: yes\nbackend: {s}\nkeys: {s}\ndb: {s}\n", .{ wrapped.backend, paths.keys_path, paths.db_path });
}

