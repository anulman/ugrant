        } else if (std.mem.eql(u8, arg, "--scope")) {
            i += 1;
            scope = args[i];
        } else if (std.mem.eql(u8, arg, "--redirect-uri")) {
            i += 1;
            redirect_uri = args[i];
        } else if (std.mem.eql(u8, arg, "--env-kind")) {
