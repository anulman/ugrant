            i += 1;
            name = args[i];
        } else if (std.mem.eql(u8, arg, "--service")) {
            i += 1;
            service_name = args[i];
        } else if (std.mem.eql(u8, arg, "--discover")) {
            i += 1;
            discover_url = args[i];
        } else if (std.mem.eql(u8, arg, "--provider")) {
            i += 1;
            provider = args[i];
        } else if (std.mem.eql(u8, arg, "--auth-url")) {
            i += 1;
            auth_url = args[i];
        } else if (std.mem.eql(u8, arg, "--token-url")) {
