fn loadStorageConfig(allocator: std.mem.Allocator, config_path: []const u8) !StorageConfig {
    var cfg = StorageConfig{};
    const bytes = std.fs.cwd().readFileAlloc(allocator, config_path, 64 * 1024) catch |err| switch (err) {
        error.FileNotFound => return cfg,
        else => return err,
    };
    defer allocator.free(bytes);

    var in_storage = false;
    var lines = std.mem.splitScalar(u8, bytes, '\n');
    while (lines.next()) |raw_line| {
        const line = std.mem.trim(u8, raw_line, " \r\t");
        if (line.len == 0 or line[0] == '#') continue;
        if (line[0] == '[' and line[line.len - 1] == ']') {
            in_storage = std.mem.eql(u8, std.mem.trim(u8, line[1 .. line.len - 1], " \t"), "storage");
            continue;
        }
        if (!in_storage) continue;
        const eq = std.mem.indexOfScalar(u8, line, '=') orelse continue;
        const key = std.mem.trim(u8, line[0..eq], " \t");
        const value = std.mem.trim(u8, line[eq + 1 ..], " \t");
        if (std.mem.eql(u8, key, "refresh_skew_seconds")) cfg.refresh_skew_seconds = try std.fmt.parseInt(i64, value, 10) else if (std.mem.eql(u8, key, "lease_timeout_seconds")) cfg.lease_timeout_seconds = try std.fmt.parseInt(i64, value, 10) else if (std.mem.eql(u8, key, "poll_interval_ms")) cfg.poll_interval_ms = try std.fmt.parseInt(u64, value, 10) else if (std.mem.eql(u8, key, "max_wait_seconds")) cfg.max_wait_seconds = try std.fmt.parseInt(i64, value, 10);
    }
    return cfg;
}

fn envTruthy(name: []const u8) bool {
    const raw = std.posix.getenv(name) orelse return false;
    return std.mem.eql(u8, raw, "1") or std.ascii.eqlIgnoreCase(raw, "true") or std.ascii.eqlIgnoreCase(raw, "yes");
}

fn commandExists(allocator: std.mem.Allocator, name: []const u8) bool {
    const result = std.process.Child.run(.{ .allocator = allocator, .argv = &.{ "sh", "-c", "command -v \"$1\" >/dev/null 2>&1", "sh", name }, .max_output_bytes = 0 }) catch return false;
    return switch (result.term) {
        .Exited => |code| code == 0,
        else => false,
    };
}

fn backendAvailable(allocator: std.mem.Allocator, backend: []const u8) bool {
    if (std.mem.eql(u8, backend, "tpm2")) {
        if (envTruthy("UGRANT_TEST_TPM2_AVAILABLE")) return true;
        return commandExists(allocator, "tpm2_create") and commandExists(allocator, "tpm2_unseal") and commandExists(allocator, "tpm2_createprimary") and commandExists(allocator, "tpm2_load");
    }
    if (std.mem.eql(u8, backend, "platform-secure-store")) {
        if (envTruthy("UGRANT_TEST_PLATFORM_STORE_AVAILABLE")) return true;
        return commandExists(allocator, "secret-tool") and std.posix.getenv("DBUS_SESSION_BUS_ADDRESS") != null;
    }
    if (std.mem.eql(u8, backend, "passphrase")) return true;
    if (std.mem.eql(u8, backend, "insecure-keyfile")) return true;
    return false;
}

fn resolvePlatformSecureStoreBackend(tpm2_available: bool, platform_store_available: bool) ![]const u8 {
    if (builtin.os.tag == .linux and tpm2_available) return "tpm2";
    if (platform_store_available) return "platform-secure-store";
    return error.WrapBackendUnavailable;
}

fn resolveBackendChoice(requested_backend: ?[]const u8, allow_insecure: bool, tpm2_available: bool, platform_store_available: bool) ![]const u8 {
    if (requested_backend) |backend| {
        if (std.mem.eql(u8, backend, "insecure-keyfile")) {
            if (!allow_insecure) return error.InsecureBackendRequiresOptIn;
            return "insecure-keyfile";
        }
        if (std.mem.eql(u8, backend, "platform-secure-store")) return resolvePlatformSecureStoreBackend(tpm2_available, platform_store_available);
        if (std.mem.eql(u8, backend, "tpm2")) {
            if (!tpm2_available) return error.WrapBackendUnavailable;
            return "tpm2";
        }
        if (std.mem.eql(u8, backend, "passphrase")) return "passphrase";
        return error.WrapBackendUnavailable;
    }

    if (tpm2_available) return "tpm2";
    if (platform_store_available) return "platform-secure-store";
    return "passphrase";
}

fn chooseInitBackend(allocator: std.mem.Allocator, requested_backend: ?[]const u8, allow_insecure: bool) ![]const u8 {
    return resolveBackendChoice(requested_backend, allow_insecure, backendAvailable(allocator, "tpm2"), backendAvailable(allocator, "platform-secure-store"));
}

fn initOrLoadKeys(allocator: std.mem.Allocator, keys_path: []const u8, requested_backend: ?[]const u8, allow_insecure: bool) !WrappedDekRecord {
    if (try fileExists(keys_path)) return loadWrappedDek(allocator, keys_path);

    const backend = try chooseInitBackend(allocator, requested_backend, allow_insecure);
    const wrap_secret = try wrapSecretForBackend(allocator, backend, "Create passphrase for ugrant: ", keys_path, null);
    defer allocator.free(wrap_secret.secret);

    var dek: [dek_len]u8 = undefined;
    crypto.random.bytes(&dek);
    const record = try wrapDekForBackend(allocator, backend, 1, wrap_secret.secret, &dek, wrap_secret.secret_ref, wrap_secret.tpm2_pub_b64, wrap_secret.tpm2_priv_b64);
    errdefer freeWrappedDekRecord(allocator, record);
    try saveWrappedDek(keys_path, record);
    return record;
}

const WrapSecret = struct {
    secret: []u8,
    secret_ref: ?[]const u8 = null,
    tpm2_pub_b64: ?[]const u8 = null,
    tpm2_priv_b64: ?[]const u8 = null,
};

fn wrapDekForBackend(allocator: std.mem.Allocator, backend: []const u8, key_version: u32, passphrase: []const u8, dek: *const [dek_len]u8, secret_ref: ?[]const u8, tpm2_pub_b64: ?[]const u8, tpm2_priv_b64: ?[]const u8) !WrappedDekRecord {
    var salt: [kdf_salt_len]u8 = undefined;
    var nonce: [gcm_nonce_len]u8 = undefined;
    crypto.random.bytes(&salt);
    crypto.random.bytes(&nonce);
    var key: [dek_len]u8 = undefined;
    deriveWrapKey(&key, passphrase, &salt);
    var ct: [dek_len]u8 = undefined;
    var tag: [gcm_tag_len]u8 = undefined;
    crypto.aead.aes_gcm.Aes256Gcm.encrypt(&ct, &tag, dek, "ugrant-dek-wrap", nonce, key);
    var combined = try allocator.alloc(u8, dek_len + gcm_tag_len);
    @memcpy(combined[0..dek_len], &ct);
    @memcpy(combined[dek_len..], &tag);
    defer allocator.free(combined);
    return .{
        .version = schema_version,
        .backend = try allocator.dupe(u8, backend),
        .key_version = key_version,
        .salt_b64 = try b64EncodeAlloc(allocator, &salt),
        .nonce_b64 = try b64EncodeAlloc(allocator, &nonce),
        .ciphertext_b64 = try b64EncodeAlloc(allocator, combined),
        .created_at = try std.fmt.allocPrint(allocator, "{}", .{nowTs()}),
        .secret_ref = if (secret_ref) |v| try allocator.dupe(u8, v) else null,
        .tpm2_pub_b64 = if (tpm2_pub_b64) |v| try allocator.dupe(u8, v) else null,
        .tpm2_priv_b64 = if (tpm2_priv_b64) |v| try allocator.dupe(u8, v) else null,
    };
}

fn unwrapDek(allocator: std.mem.Allocator, record: WrappedDekRecord) ![]u8 {
    const passphrase = try wrapSecretForBackend(allocator, record.backend, "Unlock ugrant passphrase: ", null, record);
    defer allocator.free(passphrase.secret);

    return unwrapDekWithSecret(allocator, record, passphrase.secret);
}

fn wrapSecretForBackend(allocator: std.mem.Allocator, backend: []const u8, prompt: []const u8, keys_path: ?[]const u8, record: ?WrappedDekRecord) !WrapSecret {
    if (std.mem.eql(u8, backend, "insecure-keyfile")) return .{ .secret = try allocator.dupe(u8, "insecure-local-keyfile") };
    if (std.mem.eql(u8, backend, "passphrase")) return .{ .secret = try promptSecret(allocator, prompt) };
    if (std.mem.eql(u8, backend, "platform-secure-store")) return platformStoreWrapSecret(allocator, keys_path, record);
    if (std.mem.eql(u8, backend, "tpm2")) return tpm2WrapSecret(allocator, record);
    return error.UnsupportedWrapBackend;
}

fn unwrapDekWithSecret(allocator: std.mem.Allocator, record: WrappedDekRecord, passphrase: []const u8) ![]u8 {
    const salt = try b64DecodeAlloc(allocator, record.salt_b64);
    defer allocator.free(salt);
    const nonce = try b64DecodeAlloc(allocator, record.nonce_b64);
    defer allocator.free(nonce);
    const ciphertext = try b64DecodeAlloc(allocator, record.ciphertext_b64);
    defer allocator.free(ciphertext);
    if (ciphertext.len != dek_len + gcm_tag_len) return error.InvalidWrappedDek;
    var key: [dek_len]u8 = undefined;
    deriveWrapKey(&key, passphrase, salt);
    var out: [dek_len]u8 = undefined;
    const tag: [gcm_tag_len]u8 = ciphertext[dek_len..][0..gcm_tag_len].*;
    crypto.aead.aes_gcm.Aes256Gcm.decrypt(&out, ciphertext[0..dek_len], tag, "ugrant-dek-wrap", nonce[0..gcm_nonce_len].*, key) catch return error.InvalidPassphrase;
    return allocator.dupe(u8, &out);
}

fn deriveWrapKey(out: *[dek_len]u8, passphrase: []const u8, salt: []const u8) void {
    var h = crypto.hash.sha2.Sha256.init(.{});
    h.update(passphrase);
    h.update(salt);
    h.final(out);
}

fn saveWrappedDek(keys_path: []const u8, record: WrappedDekRecord) !void {
    var list = std.ArrayList(u8){};
    defer list.deinit(std.heap.page_allocator);
    try list.writer(std.heap.page_allocator).print("{{\"version\":{},\"backend\":\"{s}\",\"key_version\":{},\"salt_b64\":\"{s}\",\"nonce_b64\":\"{s}\",\"ciphertext_b64\":\"{s}\",\"created_at\":\"{s}\"", .{ record.version, record.backend, record.key_version, record.salt_b64, record.nonce_b64, record.ciphertext_b64, record.created_at });
    if (record.secret_ref) |v| try list.writer(std.heap.page_allocator).print(",\"secret_ref\":\"{s}\"", .{v});
    if (record.tpm2_pub_b64) |v| try list.writer(std.heap.page_allocator).print(",\"tpm2_pub_b64\":\"{s}\"", .{v});
    if (record.tpm2_priv_b64) |v| try list.writer(std.heap.page_allocator).print(",\"tpm2_priv_b64\":\"{s}\"", .{v});
    try list.append(std.heap.page_allocator, '}');
    const rendered = try list.toOwnedSlice(std.heap.page_allocator);
    defer std.heap.page_allocator.free(rendered);
    const tmp_path = try std.fmt.allocPrint(std.heap.page_allocator, "{s}.tmp", .{keys_path});
    defer std.heap.page_allocator.free(tmp_path);
    {
        var file = try std.fs.createFileAbsolute(tmp_path, .{ .truncate = true });
        defer file.close();
        try file.writeAll(rendered);
    }
    try std.fs.renameAbsolute(tmp_path, keys_path);
}

