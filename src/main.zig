        std.process.exit(2);
    }

    var temp_arena = std.heap.ArenaAllocator.init(allocator);
    defer temp_arena.deinit();
    const ta = temp_arena.allocator();

    var name: ?[]const u8 = null;
    var service_name: ?[]const u8 = null;
    var discover_url: ?[]const u8 = null;
